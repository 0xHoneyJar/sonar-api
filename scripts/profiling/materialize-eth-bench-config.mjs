#!/usr/bin/env node
/**
 * Generate disposable Mission 3 Envio configs from production config + floor
 * registry. The registry remains the membership authority; generated profiles
 * are benchmark artifacts, never a second address list.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import YAML from "yaml";

const root = resolve(import.meta.dirname, "../..");
const args = process.argv.slice(2);

function option(name) {
  const index = args.indexOf(name);
  return index < 0 ? undefined : args[index + 1];
}

const profile = option("--profile") ?? "w1";
const output = resolve(root, option("--output") ?? "config.bench.eth.yaml");
const checkOnly = args.includes("--check");
const startBlock = option("--start") ? Number(option("--start")) : undefined;
const endBlock = option("--end") ? Number(option("--end")) : undefined;
const headsPath = option("--heads");
if ((startBlock === undefined) !== (endBlock === undefined)) {
  throw new Error("--start and --end must be supplied together");
}
if (startBlock !== undefined && startBlock >= endBlock) {
  throw new Error("--start must be less than --end");
}
if (!["quiet", "w1", "w1-control", "multichain"].includes(profile)) {
  throw new Error(`unsupported --profile ${profile}`);
}

const production = YAML.parse(readFileSync(resolve(root, "config.yaml"), "utf8"), {
  intAsBigInt: true,
});
const registry = JSON.parse(
  readFileSync(resolve(root, "scripts/profiling/floor-registry.w1.json"), "utf8"),
);

function address(value) {
  if (typeof value === "bigint") {
    return `0x${value.toString(16).padStart(40, "0")}`;
  }
  return String(value).toLowerCase();
}

function cloneDeployment(deployment, minimumStart) {
  return {
    ...deployment,
    ...(deployment.address
      ? { address: deployment.address.map(address) }
      : {}),
    ...(deployment.start_block === undefined || minimumStart === undefined
      ? {}
      : {
          start_block: Math.max(
            Number(deployment.start_block),
            minimumStart,
          ),
        }),
  };
}

const eth = production.chains.find((chain) => Number(chain.id) === 1);
const tracked = eth.contracts.find(
  (contract) => contract.name === "EthTrackedErc721",
);
if (!tracked) throw new Error("production EthTrackedErc721 deployment missing");

const allowed = new Set(
  registry
    .filter((row) => Number(row.chain_id) === 1 && !row.blocked)
    .map((row) => String(row.contract).toLowerCase()),
);
const w1 = tracked.address.map(address).filter((item) => allowed.has(item));
if (tracked.address.length !== 14 || w1.length !== 13) {
  throw new Error(
    `unexpected governed membership production=${tracked.address.length} eligible=${w1.length}`,
  );
}

const nouns = "0x9c8ff314c9bc7f6e59a9d9225fb22946427edc03";
const ethDeployments = [
  {
    name: "EthTrackedErc721",
    address: profile === "quiet" ? [nouns] : w1,
  },
];
if (profile === "w1-control") {
  const seaport = eth.contracts.find(
    (contract) => contract.name === "Seaport",
  );
  if (!seaport) throw new Error("production Seaport deployment missing");
  ethDeployments.push(cloneDeployment(seaport));
}

function windowedChain(chain, contracts, start, end) {
  const configuredStart = start ?? Number(chain.start_block);
  const configuredEnd = end;
  return {
    id: Number(chain.id),
    start_block: configuredStart,
    ...(configuredEnd === undefined ? {} : { end_block: configuredEnd }),
    contracts: contracts.map((contract) =>
      cloneDeployment(contract, configuredStart),
    ),
  };
}

const chains = [
  windowedChain(eth, ethDeployments, startBlock, endBlock),
];
if (profile === "multichain") {
  if (!headsPath || startBlock === undefined || endBlock === undefined) {
    throw new Error("multichain requires --heads plus --start/--end");
  }
  const heads = JSON.parse(readFileSync(resolve(root, headsPath), "utf8"));
  const window = endBlock - startBlock;
  for (const chain of production.chains) {
    const chainId = Number(chain.id);
    if (chainId === 1) continue;
    const end = Number(heads[String(chainId)]);
    if (!Number.isFinite(end)) throw new Error(`missing head for chain ${chainId}`);
    chains.push(
      windowedChain(
        chain,
        chain.contracts,
        end - window,
        end,
      ),
    );
  }
}

const document = {
  name: `sonar-belt-bench-${profile}`,
  schema: relative(dirname(output), resolve(root, "schema.graphql")),
  contracts: production.contracts,
  chains,
};
const header =
  "# GENERATED by scripts/profiling/materialize-eth-bench-config.mjs\n" +
  "# Source: config.yaml + floor-registry.w1.json; do not hand-edit addresses.\n";
const rendered = header + YAML.stringify(document, { lineWidth: 0 });

if (checkOnly) {
  const current = readFileSync(output, "utf8");
  if (current !== rendered) {
    console.error(`DRIFT ${output}`);
    process.exit(1);
  }
  console.log(`ok ${output} profile=${profile} members=${w1.length}`);
} else {
  writeFileSync(output, rendered);
  console.log(
    `wrote ${output} profile=${profile} chains=${chains.length} members=${w1.length}`,
  );
}
